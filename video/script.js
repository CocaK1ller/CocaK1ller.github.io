// 根据视频时间更新信息的函数
function updateInfo() {
    // 获取视频当前的帧数（假设视频有400帧）
    const currentFrame = Math.floor((video.currentTime / video.duration) * infoData.length);
    // 更新显示信息
    info.textContent = infoData[currentFrame];
}

function generateString() {
    // 假设这是你要传递的字符串
    let str = "这是一段关于视频的基本信息描述。这里将显示与视频同步的详细信息。";
    // 对字符串进行处理，添加一级标题和二级标题
    let strWithTitles = `<h1>视频信息总览</h1><h2>视频基本信息</h2>${str}<h2>同步信息详情</h2>`;
    return strWithTitles;
}

// 监听视频时间更新事件
video.addEventListener('timeupdate', updateInfo);

// 监听切换信息按钮的点击事件
toggleInfoBtn.addEventListener('click', function() {
    infoVisible =!infoVisible;
    if (infoVisible) {
        info.style.display = 'block';
        toggleInfoBtn.textContent = '<i class="fas fa-info-circle"></i> 显示/隐藏信息';
    } else {
        info.style.display = 'none';
        toggleInfoBtn.textContent = '<i class="fas fa-info-circle"></i> 显示信息';
    }
});